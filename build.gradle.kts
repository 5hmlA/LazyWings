fun currentDate(): String {
    // 获取当前 UTC+8 的时间
    val zoneId = ZoneId.of("Asia/Shanghai") // UTC+8
    val currentDateTime = ZonedDateTime.now(zoneId)
    // 定义格式
    val formatter = DateTimeFormatter.ofPattern("yyMMdd")
    // 格式化输出
    return currentDateTime.format(formatter)

}

